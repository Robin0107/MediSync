    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
         if(paciente.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            try{
                String query = "INSERT INTO ordenes_medicas(id_pacientes,descripcion,id_empleados) VALUES ('" + id_pacientes.getText() + "','" + receta.getText() + "','" + id_medico.getText() + "')";
                PreparedStatement psz = cn.prepareStatement(query);
                psz.executeUpdate();
                JOptionPane.showMessageDialog(null, "ORDEN MEDICA REGISTRADA");
                limpiar_campos();
           
            } catch(Exception error){
                JOptionPane.showMessageDialog(null, "Error: " + error);
            }
        }
    }//GEN-LAST:event_agregarActionPerformed

